#!/usr/bin/env python3
"""
Converts dataset/products.xlsx into database/generated_seed.sql.

Workflow:
    1. Edit dataset/products.xlsx (add products / search terms)
    2. Run: python dataset/import_dataset.py
    3. Open database/generated_seed.sql in pgAdmin and run it
       (safe to re-run — uses the same NOT EXISTS / ON CONFLICT
       guards as database/seed.sql, so it won't create duplicates)

Requires: openpyxl  (pip install openpyxl --break-system-packages)
"""

import openpyxl
import sys
from pathlib import Path

INPUT_FILE = Path(__file__).parent / "products.xlsx"
OUTPUT_FILE = Path(__file__).parent.parent / "database" / "generated_seed.sql"

VALID_TERM_TYPES = {"official", "alias", "regional", "typo", "hashtag"}


def esc(value) -> str:
    """Escape a value for safe inclusion in a SQL string literal."""
    if value is None:
        return "NULL"
    text = str(value).replace("'", "''")
    return f"'{text}'"


def load_products(ws):
    rows = list(ws.iter_rows(min_row=2, values_only=True))
    products = []
    for row in rows:
        if not row or not row[0]:
            continue
        name, category, sci_name, desc, price, stock, image_url = (list(row) + [None] * 7)[:7]
        products.append({
            "product_name": name,
            "category": category,
            "scientific_name": sci_name,
            "description": desc,
            "price": price if price not in (None, "") else "NULL",
            "stock_quantity": stock if stock not in (None, "") else 0,
            "image_url": image_url,
        })
    return products


def load_search_terms(ws):
    rows = list(ws.iter_rows(min_row=2, values_only=True))
    terms = []
    errors = []
    for i, row in enumerate(rows, start=2):
        if not row or not row[0]:
            continue
        product_name, term, term_type, language = (list(row) + [None] * 4)[:4]
        if term_type not in VALID_TERM_TYPES:
            errors.append(f"  Row {i}: invalid term_type '{term_type}' for '{term}' "
                           f"(must be one of {sorted(VALID_TERM_TYPES)})")
            continue
        terms.append({
            "product_name": product_name,
            "search_term": term,
            "term_type": term_type,
            "language": language,
        })
    return terms, errors


def main():
    if not INPUT_FILE.exists():
        print(f"ERROR: {INPUT_FILE} not found.")
        sys.exit(1)

    wb = openpyxl.load_workbook(INPUT_FILE, data_only=True)
    if "products" not in wb.sheetnames or "search_terms" not in wb.sheetnames:
        print("ERROR: workbook must contain 'products' and 'search_terms' sheets.")
        sys.exit(1)

    products = load_products(wb["products"])
    terms, errors = load_search_terms(wb["search_terms"])

    if errors:
        print("Found problems in search_terms — fix these rows before importing:")
        for e in errors:
            print(e)
        sys.exit(1)

    known_products = {p["product_name"] for p in products}
    orphan_terms = [t for t in terms if t["product_name"] not in known_products]
    if orphan_terms:
        print("Found search_terms rows whose product_name has no matching row in 'products':")
        for t in orphan_terms:
            print(f"  '{t['search_term']}' -> '{t['product_name']}'")
        sys.exit(1)

    lines = [
        "-- AUTO-GENERATED by dataset/import_dataset.py — do not edit by hand.",
        "-- Source: dataset/products.xlsx",
        "-- Safe to re-run: uses NOT EXISTS / ON CONFLICT guards.",
        "",
    ]

    # Ensure categories referenced exist
    categories = sorted({p["category"] for p in products if p["category"]})
    for cat in categories:
        lines.append(
            f"INSERT INTO categories (category_name) VALUES ({esc(cat)}) "
            f"ON CONFLICT (category_name) DO NOTHING;"
        )
    lines.append("")

    # Products
    for p in products:
        lines.append(
            "INSERT INTO products (category_id, product_name, scientific_name, description, price, stock_quantity, image_url)\n"
            "SELECT category_id, "
            f"{esc(p['product_name'])}, {esc(p['scientific_name'])}, {esc(p['description'])}, "
            f"{p['price']}, {p['stock_quantity']}, {esc(p['image_url'])}\n"
            f"FROM categories WHERE category_name = {esc(p['category'])}\n"
            f"AND NOT EXISTS (SELECT 1 FROM products WHERE product_name = {esc(p['product_name'])});"
        )
    lines.append("")

    # Search terms
    for t in terms:
        lines.append(
            "INSERT INTO search_terms (product_id, search_term, term_type, language)\n"
            "SELECT product_id, "
            f"{esc(t['search_term'])}, {esc(t['term_type'])}, {esc(t['language'])}\n"
            f"FROM products WHERE product_name = {esc(t['product_name'])}\n"
            "ON CONFLICT (product_id, search_term) DO NOTHING;"
        )

    OUTPUT_FILE.parent.mkdir(exist_ok=True)
    OUTPUT_FILE.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {OUTPUT_FILE} — {len(products)} product(s), {len(terms)} search term(s).")


if __name__ == "__main__":
    main()
